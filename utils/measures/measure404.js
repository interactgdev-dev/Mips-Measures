const anesthesiaCptSet = new Set(
  splitCodes(ANESTHESIA_CPT_RAW.replace(/\n/g, " ")).map((c) => normalizeCode(c)).filter(Boolean),
);

const measure404SmokingAbstinenceDenominator = async (collection, records) => {
  await bulkUpdateRecords(collection, records, (record) => {
    const age = getNumericAge(record);
    const ageOk = !Number.isNaN(age) && age >= 18;

    const procedureTokens = getProcedureTokens(record);

    const hasAnesthesiaCpt = procedureTokens.some((c) => anesthesiaCptSet.has(c));

    const inDenominator = ageOk && hasAnesthesiaCpt;

    return {
      CPT404: inDenominator ? 1 : 0,
      M404: inDenominator ? 1 : 0,
      N404_MET: 0,
      N404_EXCEPTION: 0,
      N404_NOT_MET: 0,
      QDC404: "",
    };
  });

  return {
    message: "Measure 404 (denominator) processed successfully",
    totalRecords: records.length,
  };
};

module.exports = {
  measure404SmokingAbstinenceDenominator,
  measure404: measure404SmokingAbstinenceDenominator,
};
